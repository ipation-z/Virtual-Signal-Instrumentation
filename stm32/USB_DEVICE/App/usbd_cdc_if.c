/**
  * @brief  Data received over USB OUT endpoint are sent over CDC interface
  *         through this function.
  *
  *         @note
  *         This function will issue a NAK packet on any OUT packet received on
  *         USB endpoint until exiting this function. If you exit this function
  *         before transfer is complete on CDC interface (ie. using DMA controller)
  *         it will result in receiving more data while previous ones are still
  *         not sent.
  *
  * @param  Buf: Buffer of data to be received
  * @param  Len: Number of data received (in bytes)
  * @retval Result of the operation: USBD_OK if all operations are OK else USBD_FAIL
  */
static int8_t CDC_Receive_FS(uint8_t* Buf, uint32_t *Len)
{
  /* USER CODE BEGIN 6 */
  
  // 逐個位元組通過狀態機解析，無懼 USB 拆包/斷包
  for (uint32_t i = 0; i < *Len; i++) {
      uint8_t ch = Buf[i];
      switch (rx_state) {
          case W_STATE_SOF1:
              if (ch == 0x5A) rx_state = W_STATE_SOF2;
              break;
          case W_STATE_SOF2:
              if (ch == 0xA5) rx_state = W_STATE_CMD;
              else rx_state = W_STATE_SOF1;
              break;
          case W_STATE_CMD:
              r_cmd = ch; // 應為 0x01
              rx_state = W_STATE_WAVE;
              break;
          case W_STATE_WAVE:
              r_wave = ch;
              rx_state = W_STATE_FREQ_H;
              break;
          case W_STATE_FREQ_H:
              r_freq_h = ch;
              rx_state = W_STATE_FREQ_L;
              break;
          case W_STATE_FREQ_L:
              r_freq_l = ch;
              rx_state = W_STATE_VPP;
              break;
          case W_STATE_VPP:
              r_vpp = ch;
              rx_state = W_STATE_CHK;
              break;
          case W_STATE_CHK: {
              // 驗證校驗和 (CMD + WAVE + FREQ_H + FREQ_L + VPP)
              uint8_t calc_chk = (r_cmd + r_wave + r_freq_h + r_freq_l + r_vpp) & 0xFF;
              if (ch == calc_chk) {
                  uint16_t frequency = (r_freq_h << 8) | r_freq_l;
                  // 驗證成功，去調用波形更新函數
                  Setup_Waveform(r_wave, frequency, r_vpp);
              }
              rx_state = W_STATE_SOF1; // 處理完畢，重置狀態
              break;
          }
          default:
              rx_state = W_STATE_SOF1;
              break;
      }
  }

  // 【致命修復】：重新設定接收緩衝區並開啟下一次 USB 接收！
  USBD_CDC_SetRxBuffer(&hUsbDeviceFS, UserRxBufferFS);
  USBD_CDC_ReceivePacket(&hUsbDeviceFS);

  return (USBD_OK);
  /* USER CODE END 6 */
}

/**
  * @brief  CDC_Transmit_FS
  *         Data to send over USB IN endpoint are sent over CDC interface
  *         through this function.
  *         @note
  *
  *
  * @param  Buf: Buffer of data to be sent
  * @param  Len: Number of data to be sent (in bytes)
  * @retval USBD_OK if all operations are OK else USBD_FAIL or USBD_BUSY
  */
uint8_t CDC_Transmit_FS(uint8_t* Buf, uint16_t Len)
{
  uint8_t result = USBD_OK;
  /* USER CODE BEGIN 7 */
  USBD_CDC_HandleTypeDef *hcdc = (USBD_CDC_HandleTypeDef*)hUsbDeviceFS.pClassData;
  if (hcdc->TxState != 0){
    return USBD_BUSY;
  }
  USBD_CDC_SetTxBuffer(&hUsbDeviceFS, Buf, Len);
  result = USBD_CDC_TransmitPacket(&hUsbDeviceFS);
  /* USER CODE END 7 */
  return result;
}

